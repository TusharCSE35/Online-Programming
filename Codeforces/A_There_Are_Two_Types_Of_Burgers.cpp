#include<bits/stdc++.h>
using namespace std;

int main()
{
   int t,b,p,f,h,c,x,y,z;

   cin >> t;
   while(t--)
   {
   	int x=0,y=0,sum = 0;
   	cin >> b >> p >> f >> h >> c;
   	if(h > c)
   	{
   		if(b >= p*2)
   		{
   		   x = h*p;
   		   b = b - p*2;
   		}
   		else
   		{
   			z = b/2;
   			x = h*z;
   			b = b - z*2;
   		}

   		if(b >= f*2)
   		{
   		   y = c*f;
   		}
        else 
        {
        	y = c*(b/2);
        }

   		sum = x + y;
   	}
   	else if(c >= h)
   	{
   		if(b >= f*2)
   		{
   		   y = c*f;
   		   b = b-f*2;
   		}
   		else
   		{
   			z = b/2;
   			y = c*z;
   			b = b - z*2;
   		}

   		if(b >= p*2)
   		{
   		   x = h*p;
   		}
   		else
   		{
   			x = h*(b/2);
   		}

   		sum = x + y;
   	}

   	cout << sum << endl;
   }

	return 0;
}