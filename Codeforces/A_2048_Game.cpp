#include<bits/stdc++.h>
using namespace std;


int main()
{
	int q,n,i,j,x;

    cin >> q;
    while(q--)
    {
        cin >> n;
        int s[n+1],cnt = 0;

        for(i = 0; i < n; i++)
        {
            cin >> s[i];
            if(s[i] == 2048)
            {
                cnt++;
            }
        }

        if(cnt > 0)
        {
            cout << "YES" << endl;
        }
        else
        {
            sort(s,s+n);
            for(i = 0; i < n-1; i++)
            {
                for(j = i+1; j < n; j++)
                {
                    x = i;
                    if(s[i] == s[j] && s[i] != 0)
                    {
                        s[j] = s[i] + s[j];
                        s[i] = 0;
                    }

                    if(s[j] == 2048)
                    {
                        cout << "YES" << endl;
                        cnt++;
                        break;
                    }
                }
                i = x;
                if(cnt > 0)
                {
                    break;
                }
            }
            if(cnt == 0)
            {
                cout << "NO" << endl;
            }
        }
    }

	return 0;
}
