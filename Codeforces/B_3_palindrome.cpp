#include<bits/stdc++.h>
using namespace std;

const int N = (int) 1e5 + 5;



int main() {

	int n,i,j;

	cin >> n;

	if(n == 1)
	{
		cout << 'a' << endl;
	}
	else if(n == 2)
	{
		cout << 'b' << 'a'  << endl;
	}
	else if(n == 3)
	{
		cout << 'b' << 'b' << 'a' << endl;
	}
	else
	{
		cout << 'a' << 'a';
		for(i = 4,j=1; i <= n; i+=2,j++)
		{
			if(j%2 != 0)
			{
				cout << 'b' << 'b';
			}
			else
			{
				cout << 'a' << 'a';
			}
		}
		if(i-n == 1 && j%2 != 0)
		{
			cout << 'b' << endl;
		}
		else if(i-n == 1 && j%2 == 0)
		{
			cout << 'a' << endl;
		}
		
	}


	return 0;
}