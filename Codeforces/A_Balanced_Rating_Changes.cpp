int main()
{
	int n,i,ct=0,dt=0;

	cin >> n;
	int a[n+1];

	for(i = 0; i < n; i++)
	{
		int pe=0,po=0,ne=0,no=0;
		cin >> a[i];
		if(a[i] >= 0)
		{
			ct++;
			if(a[i]%2 == 0)
			{
				pe++;
			}
			else
			{
				po++;
			}
		}
		else
		{
            dt++;
            if(a[i]%2 == 0)
            {
            	ne++;
            } 
            else
            {
            	no++;
            }
		}
	}




	return 0;
}