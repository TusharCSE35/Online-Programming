#include<bits/stdc++.h>
using namespace std;

int main()
{
   int N,M,K,i,j,x,sum = 0,cnt=0;

   cin >> N >> K >> M;
   int a[N];

   for(i = 0; i < N-1; i++)
   {
   	cin >> a[i];
   	sum = sum + a[i];
   }

   for(j = 0; j <= K; j++)
   {
   	 x = (sum+j)/N;
   	 if(x == M)
   	 {
   	 	cnt++;
   	 	cout << j << endl;
   	 }
   }

   if(cnt == 0)
   {
   	cout << "-1" << endl;
   }

	return 0;
}