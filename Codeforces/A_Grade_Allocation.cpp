#include<bits/stdc++.h>
using namespace std;

const int N = (int) 1e5 + 5;



int main() 
{
    long long t,n,i,m,x,sum;

    cin >> t;
    while(t--)
    {
    	cin >> n >> m;
    	long long a[n+1];
    	sum=0;
    	for(i = 0; i < n; i++)
    	{
    		cin >> a[i];
    		sum = sum+a[i];
    	}

    	x = min(sum,m);

    	cout << x << endl;
    }

	return 0;
}


