#include<bits/stdc++.h>
using namespace std;

int main()
{
	long long T,s,a,b,c,sum;

	cin >> T;
	while(T--)
	{
		sum = 0;
		cin >> s >> a >> b >> c;
		while(1)
		{
			if(s >= a)
		    {
			    sum = sum + (a*c + b);
			    s = s - a*c;
		    }
		    else
		    {
			    sum = sum + s;
			    cout << sum << endl;
			    break;
		    }
		}
	}

	return 0;
}