#include<bits/stdc++.h>
using namespace std;

int main()
{
	int N,i,z=0,e=0,r=0,o=0,n=0;
	string s;

	cin >> N >> s;
	for(i = 0; i < N; i++)
	{
		if(s[i] == 'z')
		{
			z++;
		}
		if(s[i] == 'e')
		{
			e++;
		}
		if(s[i] == 'r')
		{
			r++;
		}
		if(s[i] == 'o')
		{
			o++;
		}
		if(s[i] == 'n')
		{
			n++;
		}
   }

   if(n>0 && o >= n && e >= n)
   {
	   for(i = 1; i <= n; i++)
	    {
		    cout << "1" << " ";
	    }

	    o = o-n;
	    e = e-n;
    }

    if(z == r && o >= z && r >= z)
    {
    	for(i = 1; i <= z; i++)
    	{
    		cout << "0" << " ";
    	}
    }

	return 0;
}