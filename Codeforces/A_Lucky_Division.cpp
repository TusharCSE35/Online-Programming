#include<bits/stdc++.h>
using namespace std;

int main()
{
	int n,temp,cn = 0,cnt = 0;

	 cin >> n;

	if(n == 4 || n == 7 || n%7 == 0 || n%4 == 0 || n%47 == 0 || n%744 == 0)
	{
		cout << "YES" << endl;
	}

	else
	{
		while(n != 0)
		{
			temp = n%10;
			cn++;
			if(temp == 4 || temp == 7)
			{
				cnt++;
			}
			n = n/10;
		}

		if(cnt == cn)
		{
			cout << "YES" << endl;
		}
		else
		{
			cout << "NO" << endl;
		}
	}

	return 0;
}