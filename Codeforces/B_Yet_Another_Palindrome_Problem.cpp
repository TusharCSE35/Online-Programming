#include<bits/stdc++.h>
using namespace std;


int main()
{
	int T,N,i,j;

	cin >> T;
	while(T--){
		cin >> N;
		int A[N+1],cnt=0;

		for(i = 0; i < N; i++) cin >> A[i];
		
		for(i = 0; i < N-2; i++){
		    for(j = N-1; j >= i+2; j--){
			    if(A[i] == A[j]){
			   	    cout << "Yes" << endl;
			   	    cnt++;
			   	    break;
			    }
		    }
		    if(cnt > 0) break;
	    }
		if(cnt==0) cout << "No" << endl;
	}


	return 0;
}