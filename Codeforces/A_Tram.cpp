#include<bits/stdc++.h>
using namespace std;

int main()
{
    int T,i,a,b,x=0;

    cin >> T;
    int s[T+1];

    for(i = 0; i < T; i++)
    {
        cin >> a >> b;
        x = x - a;
        x = x + b;
        s[i] = x;
    }

    sort(s,s+T);
    cout << s[T-1] << endl;

    return 0;
}
