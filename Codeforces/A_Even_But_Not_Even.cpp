#include<bits/stdc++.h>
using namespace std;

int main()
{
	int T,M,i,j,sum,cnt;
	string n;

	cin >> T;
	while(T--)
	{
	    cin >> M >> n;

	    std::vector<char> v;
	    sum = n[0]-'0';
	    v.push_back(n[0]);
        
        cnt = 0;
	    for(i = 1; i < M; i++)
	    {
	        sum = sum + (n[i]-'0');
		    v.push_back(n[i]);

		    if(sum%2 == 0 && n[i]%2 != 0)
		    {
			    for(j = 0;j < v.size(); j++)
			    {
				    cout << v[j];
				    cnt++;
			    }
			    cout << endl;
			    cnt++;
			    break;
		    }
	    }
	    if(cnt == 0)
	    {
	        cout << "-1" <<endl;
	    }
    }

	return 0;
}

