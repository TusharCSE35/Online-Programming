#include<bits/stdc++.h>
using namespace std;

int main()
{
	int n,c,i,cnt = 0;

	cin >> n >> c;
	int a[n+1];

	for(i = 0; i < n; i++)
	{
		cin >> a[i];
	}

	for(i = 0; i < n-1; i++)
	{
		if(a[i+1] - a[i] <= c)
		{
			cnt++;
		}
		else
		{
			cnt = 0;
		}
	}

    cout << cnt + 1 << endl;


	return 0;
}