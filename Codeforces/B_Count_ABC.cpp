#include<bits/stdc++.h>
using namespace std;

int main()
{
  int n,i,cnt=0,a=0,b=0,c=0;

  cin >> n;
  char s[n+1];
  cin >> s;

  for(i = 0; i < n; i++)
  {
  	if(s[i] == 'A')
  	{
  		a++;
  		b = 0;
  		c = 0;
  	}
  	if(s[i] == 'B')
  	{
        b++;
        c = 0;
  	}
  	if(s[i] == 'C')
  	{
  		c++;
  	}
  	if(a == 1 && b == 1 && c == 1)
  	{
  		cnt++;
  		a=0,b=0,c=0;
  	}
  	else
  	{
  		a = 0,b = 0,c = 0;
  	}
  }

  cout << cnt << endl;


	return 0;
}