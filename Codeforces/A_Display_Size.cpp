#include<bits/stdc++.h>
using namespace std;

const int N = (int) 1e5 + 5;



int main()
{
	long long n,x,y,z,i,c=0;

	cin >> n;
	x = 1,y = n;
	for(i = 1; i <= n; i++)
	{
		if(n%i == 0)
		{
			z = n/i; 
			if(i > z)
			{
				c++;
				cout << x << " " << y << endl;
				break;
			}
			x = i;
			y = z;
		}
	}
	if(c == 0)
	{
		cout << n << " " << n << endl;
	}


	return 0;
}