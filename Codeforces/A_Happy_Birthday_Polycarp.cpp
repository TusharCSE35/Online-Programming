#include<bits/stdc++.h>
using namespace std;

int main()
{
	long long n,p,q,i,cnt;

	cin >> n;
	while(n--)
	{
		cin >> p;
		cnt = 0;
        for(i = 1; i < 10; i++)
        {
        	q = i;
        	while(p >= q)
        	{
        		q = (10*q) + i;
        		cnt++;
        	}
        }
        cout << cnt << endl;
    }    

	return 0;
} 

