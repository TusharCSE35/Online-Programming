#include<bits/stdc++.h>
using namespace std;

int main()
{
	int T,n,s,k,i,x,cnt;

	cin >> T;
	while(T--)
	{
		cin >> n >> s >> k;
		int a[k+1];

		for(i = 0; i < k; i++)
		{
			cin >> a[i];
		}

		if(s == k)
		{
			cout << "0" << endl;
		}
		else
		{
			cnt = 0;
			if(s < k)
			{
			    x = n-k;
			    for(i = 0; i < k; i++)
			    {
			    	if(a[i] <= x)
			    	{
			    		cnt++;
			    	}
			    }
			    cout << cnt << endl;
			}
			else
			{
				x = n-k;
				for(i = 0; i < k; i++)
				{
					if(a[i] >= s && a[i] < x)
					{
						cnt++;
					}
				}
				cout << cnt << endl;
			}

		}
	}


	return 0;
}