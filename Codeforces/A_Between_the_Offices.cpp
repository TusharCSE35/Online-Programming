#include<bits/stdc++.h>
using namespace std;

int main()
{
	int n,i,cnt=0;
	string s;

	cin >> n >> s;
	for(i = 0; i < n-1; i++)
	{
		if(s[i] != s[i+1])
		{
			cnt++;
		}
	}

	if(cnt%2 == 0 && (s[0] == s[n-1]))
	{
		cout << "NO" << endl;
	}
	else if(s[0] == 'F' && s[n-1] == 'S')
	{
		cout << "NO" << endl;
	}
	else
	{
		cout << "YES" << endl;
	}

	return 0;
}