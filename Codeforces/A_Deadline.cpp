#include<bits/stdc++.h>
using namespace std;

int main()
{
    int T,n,d,i,x,y,cnt;

    cin >> T;

    while(T--)
    {
        cin >> n >> d;
        if(n >= d)
        {
        	cout << "YES" << endl;
        }
        else
        {
        	cnt = 0;
        	for(i = 1; i <= n; i++)
        	{
        		x = i + d/(i+1);
        		y = d%(i+1);

        		if(y == 0)
        		{
        			if(x <= n)
        			{
        				cout << "YES" << endl;
        				cnt++;
        				break;
        			}
        			else
        			{
        				continue;
        			}

        		}
        		else
        		{
        			if(x+1 <= n)
        			{
        				cout << "YES" << endl;
        				cnt++;
        				break;
        			}
        			else
        			{
        				continue;
        			}
        		}

        	}

        	if(cnt == 0)
        	{
        		cout << "NO" << endl;
        	}
        }

    }


	return 0;
}