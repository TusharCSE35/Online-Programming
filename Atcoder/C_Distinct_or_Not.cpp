#include<bits/stdc++.h>
using namespace std;


int main()
{
	long long N,i,cnt = 0;

    cin >> N;
    long long A[N+1];

    for(i = 0; i < N; i++)
    {
        cin >> A[i];
    }
    sort(A,A+N);
    for(i = 0; i < N-1; i++)
    {
        if(A[i] == A[i+1])
        {
            cnt++;
            cout << "NO" << endl;
            break;
        }
    }
    if(cnt == 0)
    {
        cout << "YES" << endl;
    }

	return 0;
}
