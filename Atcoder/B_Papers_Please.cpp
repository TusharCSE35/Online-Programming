#include<bits/stdc++.h>
using namespace std;
 
int main()
{
    int N,A,cnt = 0,ct = 0;
 
    cin >> N;
 
    while(N--)
    {
        cin >> A;
        if(A%2 == 0)
        {
            ct++;
            if(A%3 == 0 || A%5 == 0)
            {
                cnt++;
            }
        }
    }
 
    if(ct == cnt)
    {
        cout << "APPROVED" << endl;
    }
    else
    {
        cout << "DENIED" << endl;
    }
 
 
 
    return 0;
}