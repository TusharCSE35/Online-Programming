#include<bits/stdc++.h>
using namespace std;


int main()
{
	int T,i,j,len;
    char s[101];

    cin >> T;
    while(T--)
    {
    	int cnt = 0,x=0,y=0,z=0;
    	cin >> s;
    	len = strlen(s);

    	for(i = 0; i < len; i++)
    	{
    		if(s[i] == '1')
            {
                x = i;
                z++;
                break;
            }
    	}

        for(i = len-1; i >= x; i--)
        {
            if(s[i] == '1')
            {
                y = i;
                break;
            }
        }

        for(i = x; i <= y; i++)
        {
            if(s[i] == '0')
            {
                cnt++;
            }
        }

        if(z > 0)
        {
            cout << cnt << endl;
        }
        else
        {
           cout << "0" << endl; 
        }
        
    }

	return 0;
}
